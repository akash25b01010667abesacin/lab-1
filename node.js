

function sayHello(name) {
    console.log(`Hello ${name}`);
}

const sayHi = (name) => {
    console.log(`Hi ${name}`);
}

sayHello("John Doe");  // Output: Hello John Doe
sayHi("John Doe");     // Output: Hi John Doe

function sum(a, b) {
    return a + b;
}

console.log(sum(5, 10)); // Output: 15
